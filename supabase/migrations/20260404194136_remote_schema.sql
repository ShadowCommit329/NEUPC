CREATE OR REPLACE FUNCTION public.update_user_stats_on_solve()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
DECLARE
    v_difficulty_rating SMALLINT;
BEGIN
    SELECT difficulty_rating INTO v_difficulty_rating
    FROM problems WHERE id = NEW.problem_id;
    
    INSERT INTO user_stats (user_id, total_solved, last_solve_date)
    VALUES (NEW.user_id, 1, NEW.first_solved_at::DATE)
    ON CONFLICT (user_id) DO UPDATE SET
        total_solved = user_stats.total_solved + 1,
        last_solve_date = NEW.first_solved_at::DATE,
        solved_800 = user_stats.solved_800 + CASE WHEN v_difficulty_rating BETWEEN 800 AND 899 THEN 1 ELSE 0 END,
        solved_900 = user_stats.solved_900 + CASE WHEN v_difficulty_rating BETWEEN 900 AND 999 THEN 1 ELSE 0 END,
        solved_1000 = user_stats.solved_1000 + CASE WHEN v_difficulty_rating BETWEEN 1000 AND 1099 THEN 1 ELSE 0 END,
        solved_1100 = user_stats.solved_1100 + CASE WHEN v_difficulty_rating BETWEEN 1100 AND 1199 THEN 1 ELSE 0 END,
        solved_1200 = user_stats.solved_1200 + CASE WHEN v_difficulty_rating BETWEEN 1200 AND 1299 THEN 1 ELSE 0 END,
        solved_1300 = user_stats.solved_1300 + CASE WHEN v_difficulty_rating BETWEEN 1300 AND 1399 THEN 1 ELSE 0 END,
        solved_1400 = user_stats.solved_1400 + CASE WHEN v_difficulty_rating BETWEEN 1400 AND 1499 THEN 1 ELSE 0 END,
        solved_1500 = user_stats.solved_1500 + CASE WHEN v_difficulty_rating BETWEEN 1500 AND 1599 THEN 1 ELSE 0 END,
        solved_1600 = user_stats.solved_1600 + CASE WHEN v_difficulty_rating BETWEEN 1600 AND 1699 THEN 1 ELSE 0 END,
        solved_1700 = user_stats.solved_1700 + CASE WHEN v_difficulty_rating BETWEEN 1700 AND 1799 THEN 1 ELSE 0 END,
        solved_1800 = user_stats.solved_1800 + CASE WHEN v_difficulty_rating BETWEEN 1800 AND 1899 THEN 1 ELSE 0 END,
        solved_1900 = user_stats.solved_1900 + CASE WHEN v_difficulty_rating BETWEEN 1900 AND 1999 THEN 1 ELSE 0 END,
        solved_2000_plus = user_stats.solved_2000_plus + CASE WHEN v_difficulty_rating >= 2000 THEN 1 ELSE 0 END,
        updated_at = now();
    
    RETURN NEW;
END;
$function$
