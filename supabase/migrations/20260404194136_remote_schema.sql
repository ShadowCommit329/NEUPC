CREATE OR REPLACE FUNCTION public.log_discussion_activity()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
  IF TG_OP = 'UPDATE' THEN
    -- Log status changes
    IF OLD.status IS DISTINCT FROM NEW.status THEN
      INSERT INTO public.discussion_activity (discussion_id, user_id, action, old_value, new_value)
      VALUES (NEW.id, NEW.resolved_by, 'status_changed', OLD.status, NEW.status);
    END IF;
    
    -- Log assignment changes
    IF OLD.assigned_to IS DISTINCT FROM NEW.assigned_to THEN
      INSERT INTO public.discussion_activity (discussion_id, user_id, action, old_value, new_value)
      VALUES (NEW.id, NEW.assigned_to, 'assigned', OLD.assigned_to::text, NEW.assigned_to::text);
    END IF;
    
    -- Log resolution
    IF OLD.resolved_at IS NULL AND NEW.resolved_at IS NOT NULL THEN
      INSERT INTO public.discussion_activity (discussion_id, user_id, action, new_value)
      VALUES (NEW.id, NEW.resolved_by, 'resolved', NEW.resolved_at::text);
    END IF;
    
    -- Log pin/unpin
    IF OLD.is_pinned IS DISTINCT FROM NEW.is_pinned THEN
      INSERT INTO public.discussion_activity (discussion_id, action, new_value)
      VALUES (NEW.id, CASE WHEN NEW.is_pinned THEN 'pinned' ELSE 'unpinned' END, NEW.is_pinned::text);
    END IF;
    
    -- Log lock/unlock
    IF OLD.is_locked IS DISTINCT FROM NEW.is_locked THEN
      INSERT INTO public.discussion_activity (discussion_id, action, new_value)
      VALUES (NEW.id, CASE WHEN NEW.is_locked THEN 'locked' ELSE 'unlocked' END, NEW.is_locked::text);
    END IF;
  END IF;
  
  RETURN NEW;
END;
$function$
;

