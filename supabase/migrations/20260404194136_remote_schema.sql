CREATE OR REPLACE FUNCTION public.calculate_enrollment_progress(p_user_id uuid, p_bootcamp_id uuid)
 RETURNS integer
 LANGUAGE plpgsql
AS $function$
DECLARE
  v_total_lessons INTEGER;
  v_completed_lessons INTEGER;
  v_progress INTEGER;
BEGIN
  SELECT COALESCE(SUM(m.total_lessons), 0) INTO v_total_lessons
  FROM courses c
  JOIN modules m ON m.course_id = c.id
  WHERE c.bootcamp_id = p_bootcamp_id AND c.is_published = TRUE AND m.is_published = TRUE;
  IF v_total_lessons = 0 THEN
    RETURN 0;
  END IF;
  SELECT COUNT(*) INTO v_completed_lessons
  FROM user_progress up
  WHERE up.user_id = p_user_id 
    AND up.bootcamp_id = p_bootcamp_id 
    AND up.is_completed = TRUE;
  v_progress := ROUND((v_completed_lessons::DECIMAL / v_total_lessons) * 100);
  
  UPDATE enrollments 
  SET progress_percent = v_progress,
      completed_at = CASE WHEN v_progress >= 100 THEN NOW() ELSE NULL END,
      status = CASE WHEN v_progress >= 100 THEN 'completed'::enrollment_status ELSE status END
  WHERE user_id = p_user_id AND bootcamp_id = p_bootcamp_id;
  RETURN v_progress;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.calculate_tag_stats(p_user_id uuid)
 RETURNS jsonb
 LANGUAGE plpgsql
AS $function$
DECLARE
  tag_counts JSONB := '{}';
  tag TEXT;
  tag_array TEXT[];
  rec RECORD;
BEGIN
  -- Get all tags from problem_solves and problem_submissions for this user
  FOR rec IN 
    SELECT COALESCE(ps.tags, '{}') as tags
    FROM problem_solves ps
    WHERE ps.user_id = p_user_id
    UNION ALL
    SELECT COALESCE(psub.tags, '{}') as tags
    FROM problem_submissions psub
    WHERE psub.user_id = p_user_id AND psub.verdict = 'AC'
  LOOP
    FOREACH tag IN ARRAY rec.tags
    LOOP
      -- Normalize tag (lowercase, trim)
      tag := LOWER(TRIM(tag));
      IF tag IS NOT NULL AND tag != '' THEN
        -- Increment count for this tag
        tag_counts := jsonb_set(
          tag_counts,
          ARRAY[tag],
          to_jsonb(COALESCE((tag_counts->>tag)::integer, 0) + 1)
        );
      END IF;
    END LOOP;
  END LOOP;
  
  RETURN tag_counts;
END;
$function$
;

