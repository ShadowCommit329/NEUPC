CREATE OR REPLACE FUNCTION public.sync_platform_stats_from_history()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  current_stats jsonb;
  platform_data jsonb;
BEGIN
  -- Get current platform_stats
  SELECT platform_stats INTO current_stats
  FROM public.user_statistics
  WHERE user_id = NEW.user_id;

  -- If no stats exist, create empty object
  IF current_stats IS NULL THEN
    current_stats := '{}';
  END IF;

  -- Build updated platform data
  platform_data := COALESCE(current_stats->NEW.platform, '{}');
  platform_data := jsonb_set(platform_data, '{rating}', to_jsonb(NEW.new_rating));
  
  IF NEW.new_rating > COALESCE((platform_data->>'max_rating')::integer, 0) THEN
    platform_data := jsonb_set(platform_data, '{max_rating}', to_jsonb(NEW.new_rating));
  END IF;

  -- Update contests count
  platform_data := jsonb_set(
    platform_data, 
    '{contests}', 
    to_jsonb(get_contest_count(NEW.user_id, NEW.platform))
  );

  -- Update platform_stats
  current_stats := jsonb_set(current_stats, ARRAY[NEW.platform], platform_data);

  -- Upsert user_statistics
  INSERT INTO public.user_statistics (user_id, platform_stats, updated_at)
  VALUES (NEW.user_id, current_stats, now())
  ON CONFLICT (user_id) 
  DO UPDATE SET 
    platform_stats = current_stats,
    updated_at = now();

  RETURN NEW;
END;
$function$
;

